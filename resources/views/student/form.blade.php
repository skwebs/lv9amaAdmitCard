    <script>
    function onlyAlphabets(e, t) {
        try {
            if (window.event) {
                var charCode = window.event.keyCode;
            } else if (e) {
                var charCode = e.which;
            } else {
                return true;
            }
            if ((charCode > 64 && charCode < 91) || (charCode > 96 && charCode < 123) || (charCode == 46) || (
                    charCode == 32))
                return true;
            else
                return false;
        } catch (err) {
            alert(err.Description);
        }
    }

    function numberonly(e) {
        e = e || window.event;
        ch = e.which || e.keyCode;
        if (ch != null) {
            if ((ch >= 48 && ch <= 57) ||
                ch == 0 || ch == 8 || ch == 9 || ch == 39 || ch == 37 ||
                ch == 13 || ch == 45 || ch == 40 || ch == 41 || ch == 44 || ch == 43) return true;
        }
        return false;
    }
    </script>



